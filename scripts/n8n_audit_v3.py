def main(argv: Optional[List[str]] = None) -> int:
    parser = argparse.ArgumentParser(
        description=(
            "n8n Workflow Auditor v3 -- static analysis with "
            "line numbers and rules-driven detection."
        ),
    )
    parser.add_argument(
        "workflow", help="Path to exported n8n workflow JSON.",
    )
    parser.add_argument(
        "-o", "--output", default="audit_report.html",
        help="Output HTML path (default: audit_report.html)",
    )
    parser.add_argument(
        "-c", "--company-id", action="append", default=[],
        help="Company identifier to flag (repeatable).",
    )
    parser.add_argument(
        "--json", dest="json_out", default=None,
        help="Also write findings as JSON to this path.",
    )
    parser.add_argument(
        "--rules", dest="rules_path", default=None,
        help=(
            "Path to audit_rules.yaml. Default: auto-detect "
            "next to this script."
        ),
    )
    args = parser.parse_args(argv)

    # Load rules
    rules = load_rules(args.rules_path)
    if rules:
        rv = rules.get("version", "unknown")
        rg = rules.get("generated_at", "unknown")
        print(f"Loaded rules: version={rv}, generated={rg}")
    else:
        print("No audit_rules.yaml found -- using built-in defaults.")

    try:
        with open(args.workflow, "r", encoding="utf-8") as fh:
            raw_text = fh.read()
    except OSError as e:
        print(f"ERROR: cannot read {args.workflow}: {e}", file=sys.stderr)
        return 2

    try:
        workflow = json.loads(raw_text)
    except json.JSONDecodeError as e:
        print(f"ERROR: invalid JSON: {e}", file=sys.stderr)
        return 2

    findings = run_audit(raw_text, workflow, args.company_id, rules)

    secret_patterns = build_secret_patterns(rules)
    hardcoded_secret_ids = build_secret_rule_ids(secret_patterns)
    verdict = evaluate_verdict(findings, rules, hardcoded_secret_ids)

    html_out = render_html(
        findings, raw_text, workflow, args.workflow, verdict, rules,
    )

    with open(args.output, "w", encoding="utf-8") as fh:
        fh.write(html_out)

    if args.json_out:
        with open(args.json_out, "w", encoding="utf-8") as fh:
            json.dump({
                "rules_version": (
                    rules.get("version") if rules else "built-in"
                ),
                "rules_generated_at": (
                    rules.get("generated_at") if rules else None
                ),
                "verdict": {
                    "status": verdict.status,
                    "summary": verdict.summary,
                    "counts": verdict.counts,
                    "reasons": [
                        {
                            "code": r.code,
                            "title": r.title,
                            "detail": r.detail,
                            "remediation": r.remediation,
                            "evidence": [
                                {
                                    "rule_id": fr.rule_id,
                                    "line": fr.line,
                                    "node": fr.node_name,
                                }
                                for fr in r.finding_refs
                            ],
                        }
                        for r in verdict.reasons
                    ],
                },
                "findings": [asdict(f) for f in findings],
            }, fh, indent=2)

    c = verdict.counts
    print(
        f"Audited {args.workflow}: "
        f"VERDICT={verdict.status} | "
        f"{c.get('high', 0)} high, {c.get('medium', 0)} medium, "
        f"{c.get('low', 0)} low, {c.get('info', 0)} info. "
        f"-> {args.output}"
    )
    if verdict.status == "FAIL":
        print(f"  {verdict.summary}")
        for r in verdict.reasons:
            print(f"    [{r.code}] {r.title}")
    return 1 if verdict.status == "FAIL" else 0


if __name__ == "__main__":
    sys.exit(main())
