class _PosParser:
    """Minimal JSON parser that records (line, col) for every node, keyed by
    JSON pointer (RFC 6901). Only used to build a pointer -> position map."""

    def __init__(self, text: str) -> None:
        self.text = text
        self.i = 0
        self.line = 1
        self.col = 1
        self.positions: Dict[str, Tuple[int, int]] = {}

    def _peek(self) -> str:
        return self.text[self.i] if self.i < len(self.text) else ""

    def _advance(self) -> str:
        ch = self.text[self.i]
        self.i += 1
        if ch == "\n":
            self.line += 1
            self.col = 1
        else:
            self.col += 1
        return ch

    def _skip_ws(self) -> None:
        while self.i < len(self.text) and self.text[self.i] in " \t\r\n":
            self._advance()

    def _record(self, pointer: str, line: int, col: int) -> None:
        if pointer not in self.positions:
            self.positions[pointer] = (line, col)

    def parse(self) -> None:
        self._skip_ws()
        self._parse_value("")
        self._skip_ws()

    def _parse_value(self, pointer: str) -> None:
        self._skip_ws()
        line, col = self.line, self.col
        self._record(pointer, line, col)
        ch = self._peek()
        if ch == "{":
            self._parse_object(pointer)
        elif ch == "[":
            self._parse_array(pointer)
        elif ch == '"':
            self._parse_string()
        elif ch == "-" or ch.isdigit():
            self._parse_number()
        elif self.text.startswith("true", self.i):
            for _ in range(4):
                self._advance()
        elif self.text.startswith("false", self.i):
            for _ in range(5):
                self._advance()
        elif self.text.startswith("null", self.i):
            for _ in range(4):
                self._advance()
        else:
            self._advance()

    def _parse_object(self, pointer: str) -> None:
        assert self._advance() == "{"
        self._skip_ws()
        if self._peek() == "}":
            self._advance()
            return
        while True:
            self._skip_ws()
            if self._peek() != '"':
                return
            key = self._parse_string_value()
            self._skip_ws()
            if self._peek() == ":":
                self._advance()
            child_ptr = f"{pointer}/{_escape_ptr(key)}"
            self._parse_value(child_ptr)
            self._skip_ws()
            if self._peek() == ",":
                self._advance()
                continue
            if self._peek() == "}":
                self._advance()
                return
            return

    def _parse_array(self, pointer: str) -> None:
        assert self._advance() == "["
        self._skip_ws()
        idx = 0
        if self._peek() == "]":
            self._advance()
            return
        while True:
            child_ptr = f"{pointer}/{idx}"
            self._parse_value(child_ptr)
            idx += 1
            self._skip_ws()
            if self._peek() == ",":
                self._advance()
                continue
            if self._peek() == "]":
                self._advance()
                return
            return

    def _parse_string(self) -> None:
        self._parse_string_value()

    def _parse_string_value(self) -> str:
        assert self._advance() == '"'
        out: List[str] = []
        while self.i < len(self.text):
            ch = self._advance()
            if ch == "\\":
                if self.i < len(self.text):
                    nxt = self._advance()
                    escapes = {
                        '"': '"',
                        "\\": "\\",
                        "/": "/",
                        "b": "\b",
                        "f": "\f",
                        "n": "\n",
                        "r": "\r",
                        "t": "\t",
                    }
                    if nxt in escapes:
                        out.append(escapes[nxt])
                    elif nxt == "u":
                        hexs = ""
                        for _ in range(4):
                            if self.i < len(self.text):
                                hexs += self._advance()
                        try:
                            out.append(chr(int(hexs, 16)))
                        except ValueError:
                            out.append("?")
                    else:
                        out.append(nxt)
            elif ch == '"':
                return "".join(out)
            else:
                out.append(ch)
        return "".join(out)

    def _parse_number(self) -> None:
        while self.i < len(self.text) and self.text[self.i] in "-+0123456789.eE":
            self._advance()


def _escape_ptr(token: str) -> str:
    return token.replace("~", "~0").replace("/", "~1")


def build_position_index(raw_text: str) -> Dict[str, Tuple[int, int]]:
    parser = _PosParser(raw_text)
    try:
        parser.parse()
    except Exception:
        pass
    return parser.positions


