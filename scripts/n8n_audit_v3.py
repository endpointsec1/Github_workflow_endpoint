def run_audit(
    raw_text: str,
    workflow: Dict[str, Any],
    company_identifiers: List[str],
    rules: Optional[Dict[str, Any]],
) -> List[Finding]:
    pos_index = build_position_index(raw_text)

    # Build data-driven patterns from rules (or defaults)
    secret_patterns = build_secret_patterns(rules)
    risky_patterns = build_risky_patterns(rules)

    findings: List[Finding] = []
    findings += audit_security(
        workflow, pos_index, company_identifiers, rules, secret_patterns,
    )
    findings += audit_webhooks(workflow, pos_index)
    findings += audit_ai(workflow, pos_index, rules)
    findings += audit_error_handling(workflow, pos_index, risky_patterns)
    findings += audit_readability(workflow, pos_index)
    findings += audit_structural(workflow, pos_index)
    findings += audit_repo_hygiene(workflow, pos_index)

    findings.sort(
        key=lambda f: (SEVERITY_ORDER.get(f.severity, 9), f.line, f.rule_id),
    )
    return findings


