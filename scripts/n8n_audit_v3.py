def render_html(
    findings: List[Finding],
    raw_text: str,
    workflow: Dict[str, Any],
    input_path: str,
    verdict: Verdict,
    rules: Optional[Dict[str, Any]],
) -> str:

    def _verdict_block(v: Verdict) -> str:
        status_cls = "pass" if v.status == "PASS" else "fail"
        icon = "&#x2705;" if v.status == "PASS" else "&#x274C;"
        if v.status == "PASS":
            reasons_html = (
                '<div class="verdict-passmsg">'
                'No gating checks tripped. Review advisories below '
                'to keep the workflow healthy.'
                '</div>'
            )
        else:
            blocks = []
            for r in v.reasons:
                rems = "".join(
                    f"<li>{html.escape(step)}</li>"
                    for step in r.remediation
                )
                refs = ""
                if r.finding_refs:
                    refs_links = " ".join(
                        f'<a href="#{html.escape(fr.rule_id)}-{fr.line}">'
                        f'{html.escape(fr.rule_id)}@L{fr.line}</a>'
                        for fr in r.finding_refs[:25]
                    )
                    more = (
                        "" if len(r.finding_refs) <= 25
                        else f' <span class="muted">'
                             f'(+{len(r.finding_refs)-25} more)</span>'
                    )
                    refs = (
                        f'<div class="reason-refs">'
                        f'<b>Evidence:</b> {refs_links}{more}</div>'
                    )
                blocks.append(f"""
<div class="reason">
  <div class="reason-head">
    <span class="reason-code">{html.escape(r.code)}</span>
    <span class="reason-title">{html.escape(r.title)}</span>
  </div>
  <div class="reason-detail">{html.escape(r.detail)}</div>
  <div><b>Remediation:</b></div>
  <ol class="reason-rem">{rems}</ol>
  {refs}
</div>
""")
            reasons_html = (
                '<div class="verdict-reasons">'
                + "".join(blocks)
                + '</div>'
            )
        return f"""
<section class="verdict">
  <div class="verdict-head">
    <div class="verdict-status {status_cls}">{icon} {v.status}</div>
    <div class="verdict-summary">{html.escape(v.summary)}</div>
  </div>
  {reasons_html}
</section>
"""

    verdict_html = _verdict_block(verdict)

    counts: Dict[str, int] = defaultdict(int)
    for f in findings:
        counts[f.severity] += 1
    by_cat: Dict[str, List[Finding]] = defaultdict(list)
    for f in findings:
        by_cat[f.category].append(f)

    raw_lines = raw_text.splitlines()
    total_lines = len(raw_lines)
    wf_name = workflow.get("name", "<unnamed>")
    node_count = len(workflow.get("nodes", []) or [])
    generated = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")

    rules_version = "built-in defaults"
    if rules:
        rv = rules.get("version", "unknown")
        rg = rules.get("generated_at", "")
        rules_version = f"{rv} (generated {rg})" if rg else str(rv)

    def sev_badge(sev: str) -> str:
        return f'<span class="badge sev-{sev}">{sev.upper()}</span>'

    def snippet_for(line: int) -> str:
        if line <= 0 or line > total_lines:
            return ""
        start = max(1, line - 1)
