def audit_security(
    workflow: Dict[str, Any],
    pos_index: Dict[str, Tuple[int, int]],
    company_identifiers: List[str],
    rules: Optional[Dict[str, Any]],
    secret_patterns: List[Tuple[str, re.Pattern[str], str, str, str]],
) -> List[Finding]:
    findings: List[Finding] = []
    nodes = workflow.get("nodes", []) or []
    company_regexes = [
        (c, re.compile(rf"\b{re.escape(c)}\b", re.I))
        for c in company_identifiers if c
    ]

    # Entropy thresholds from rules
    entropy_threshold = float(
        (rules or {}).get("entropy_threshold", DEFAULT_ENTROPY_THRESHOLD)
    )
    entropy_min_len = int(
        (rules or {}).get("entropy_min_length", DEFAULT_ENTROPY_MIN_LEN)
    )
    entropy_max_len = int(
        (rules or {}).get("entropy_max_length", DEFAULT_ENTROPY_MAX_LEN)
    )

    for idx, node in enumerate(nodes):
        name = node.get("name", f"<node {idx}>")
        ntype = node.get("type", "")
        params = node.get("parameters", {}) or {}

        for path, leaf in walk(params, ["parameters"]):
            if not isinstance(leaf, str):
                continue
            if _is_credential_ref(leaf):
                continue

            # Sensitive key with literal value
            if (
                len(path) >= 2
                and isinstance(path[-1], str)
                and SENSITIVE_VALUE_KEYS.match(str(path[-1]))
                and leaf
                and not _is_expression(leaf)
                and not _is_credential_ref(leaf)
            ):
                ptr = deep_pointer(idx, path)
                line, col, _ = lookup_pos(pos_index, ptr, idx)
                findings.append(Finding(
                    category="Security",
                    severity="high",
                    rule_id="SEC-SENSITIVE-KEY-LITERAL",
                    title=f"Sensitive key '{path[-1]}' holds a literal value",
                    detail=f"value preview: {leaf[:60]!r}",
                    node_name=name, node_type=ntype,
                    line=line, col=col, json_pointer=ptr,
                    recommendation=(
                        "Move this value to an n8n credential or env var; "
                        "reference it via expression instead of hardcoding."
                    ),
                    owasp="OWASP A04:2025 (Cryptographic Failures) / A07:2025 (Auth Failures)",
                    confidence="high",
                ))

            # Pattern-based secret scanning (from rules or defaults)
            for rule_id, rx, sev, conf, desc in secret_patterns:
                m = rx.search(leaf)
                if not m:
                    continue
                if _is_expression(leaf) and "$credentials" in leaf:
                    continue
                ptr = deep_pointer(idx, path)
                line, col, _ = lookup_pos(pos_index, ptr, idx)
                findings.append(Finding(
                    category="Security",
                    severity=sev,
                    rule_id=rule_id,
                    title=desc,
                    detail=f"matched: {m.group(0)[:80]}",
                    node_name=name, node_type=ntype,
                    line=line, col=col, json_pointer=ptr,
                    recommendation=(
                        "Rotate the credential immediately if real, "
                        "and store via n8n credentials."
                    ),
                    owasp="OWASP A04:2025 / A07:2025",
                    confidence=conf,
                ))

            # High-entropy generic strings
            if (
                entropy_min_len <= len(leaf) <= entropy_max_len
                and not _is_expression(leaf)
            ):
                ent = shannon_entropy(leaf)
                key_hint = str(path[-1]).lower() if path else ""
                if ent >= entropy_threshold and any(
                    h in key_hint
                    for h in ("token", "key", "secret", "auth", "pwd", "password")
                ):
                    ptr = deep_pointer(idx, path)
                    line, col, _ = lookup_pos(pos_index, ptr, idx)
                    findings.append(Finding(
                        category="Security",
                        severity="medium",
                        rule_id="SEC-HIGH-ENTROPY",
                        title="High-entropy string in a credential-like field",
                        detail=f"entropy={ent:.2f}, len={len(leaf)}",
                        node_name=name, node_type=ntype,
                        line=line, col=col, json_pointer=ptr,
                        recommendation=(
                            "Verify this is not a hardcoded secret; "
                            "if so, move to credentials."
                        ),
                        owasp="OWASP A04:2025",
                        confidence="medium",
                    ))

            # Company identifiers
            for raw, rx in company_regexes:
                if rx.search(leaf):
                    ptr = deep_pointer(idx, path)
                    line, col, _ = lookup_pos(pos_index, ptr, idx)
                    findings.append(Finding(
                        category="Security",
                        severity="low",
                        rule_id="SEC-COMPANY-ID",
                        title=f"Company identifier '{raw}' found in workflow",
                        detail=f"value preview: {leaf[:80]!r}",
                        node_name=name, node_type=ntype,
                        line=line, col=col, json_pointer=ptr,
                        recommendation=(
                            "Confirm this identifier is safe to ship; "
                            "consider parameterizing."
                        ),
                        owasp="OWASP A09:2025 (Logging/Data exposure)",
                        confidence="medium",
                    ))

    return findings


def audit_webhooks(
    workflow: Dict[str, Any],
    pos_index: Dict[str, Tuple[int, int]],
) -> List[Finding]:
    findings: List[Finding] = []
    nodes = workflow.get("nodes", []) or []
    for idx, node in enumerate(nodes):
        ntype = (node.get("type") or "").lower()
        name = node.get("name", "")
        params = node.get("parameters", {}) or {}

        is_webhook = "webhook" in ntype or ntype.endswith(".webhook")
        is_form = "formtrigger" in ntype
        if not (is_webhook or is_form):
            continue

        # Authentication
        auth = (
            (params.get("authentication") or "").lower()
            if isinstance(params.get("authentication"), str) else ""
        )
        ptr = deep_pointer(idx, ["parameters", "authentication"])
        line, col, _ = lookup_pos(pos_index, ptr, idx)
        if auth in ("", "none"):
            findings.append(Finding(
                category="Webhook Hygiene",
                severity="high",
                rule_id="WHK-NO-AUTH",
                title=f"Webhook/trigger node '{name}' has no authentication",
                detail=f"authentication = {auth!r}",
                node_name=name, node_type=node.get("type"),
                line=line, col=col, json_pointer=ptr,
                recommendation=(
                    "Enable header/basic/JWT auth or signature "
                    "verification (e.g. HMAC)."
                ),
                owasp="OWASP A01:2025 (Broken Access Control) / A07:2025",
                confidence="high",
            ))

        # Wildcard path
        path_val = params.get("path")
        if isinstance(path_val, str) and (
            path_val == "" or path_val == "/" or "*" in path_val
        ):
            ptr = deep_pointer(idx, ["parameters", "path"])
            line, col, _ = lookup_pos(pos_index, ptr, idx)
            findings.append(Finding(
                category="Webhook Hygiene",
                severity="medium",
                rule_id="WHK-WILDCARD-PATH",
                title=f"Webhook '{name}' uses an overly broad path",
                detail=f"path = {path_val!r}",
                node_name=name, node_type=node.get("type"),
                line=line, col=col, json_pointer=ptr,
                recommendation=(
                    "Use a specific, unguessable path; "
                    "avoid wildcards on public webhooks."
                ),
                owasp="OWASP A01:2025",
                confidence="medium",
            ))

    return findings


def audit_ai(
    workflow: Dict[str, Any],
    pos_index: Dict[str, Tuple[int, int]],
    rules: Optional[Dict[str, Any]],
) -> List[Finding]:
    findings: List[Finding] = []
    nodes = workflow.get("nodes", []) or []
    connections = workflow.get("connections", {}) or {}

    # Read AI config from rules (or use defaults)
    ai_prefixes: List[str] = (rules or {}).get(
        "ai_node_prefixes", DEFAULT_AI_NODE_PREFIXES
    )
    guardrail_kws: List[str] = (rules or {}).get(
        "guardrail_keywords", DEFAULT_GUARDRAIL_KEYWORDS
    )
    max_prompt_len: int = int(
        (rules or {}).get("max_prompt_length", DEFAULT_MAX_PROMPT_LENGTH)
    )

    # Build guardrail regex from keywords
    guardrail_rx = re.compile(
        r"(?i)" + "|".join(re.escape(kw) for kw in guardrail_kws)
    )

    # Collect outgoing edges
    out_edges: Dict[str, List[str]] = defaultdict(list)
    for src, conn in connections.items():
        if not isinstance(conn, dict):
            continue
        for _kind, branches in conn.items():
            if not isinstance(branches, list):
                continue
            for branch in branches:
                if not isinstance(branch, list):
                    continue
                for tgt in branch:
                    if isinstance(tgt, dict) and tgt.get("node"):
                        out_edges[src].append(tgt["node"])

    # Triggers
    trigger_types = ("webhook", "formtrigger", "trigger")
    triggers = [
        n["name"] for n in nodes
        if isinstance(n.get("type"), str)
        and any(t in n["type"].lower() for t in trigger_types)
    ]

    # AI nodes (driven by prefixes from rules)
    ai_nodes = [
        (idx, n) for idx, n in enumerate(nodes)
        if isinstance(n.get("type"), str)
        and any(n["type"].startswith(prefix) for prefix in ai_prefixes)
    ]

    for idx, node in ai_nodes:
        name = node.get("name", "")
        ntype = node.get("type", "")
        params = node.get("parameters", {}) or {}

        # 1) Prompt-injection exposure
        if triggers:
            visited: set = set()
            stack = list(triggers)
            reached = False
            while stack:
                cur = stack.pop()
                if cur in visited:
                    continue
                visited.add(cur)
                if cur == name:
                    reached = True
                    break
                for nxt in out_edges.get(cur, []):
                    stack.append(nxt)
            if reached:
                ptr = deep_pointer(idx, [])
                line, col, _ = lookup_pos(pos_index, ptr, idx)
                findings.append(Finding(
                    category="AI / LLM",
                    severity="high",
                    rule_id="AI-PROMPT-INJECTION-PATH",
                    title=f"Untrusted input may reach AI node '{name}'",
                    detail=(
                        "A trigger node has a directed path to this LLM node; "
                        "inputs must be treated as untrusted."
                    ),
                    node_name=name, node_type=ntype,
                    line=line, col=col, json_pointer=ptr,
                    recommendation=(
                        "Sanitize / validate inputs, add a system-prompt "
                        "guardrail, and apply an output filter."
                    ),
                    owasp="OWASP LLM01:2025 (Prompt Injection)",
                    confidence="high",
                ))

        # 2) No guardrail language in prompt
        prompt_text = ""
        if isinstance(params.get("text"), str):
            prompt_text = params["text"]
        elif isinstance(params.get("messages"), (dict, list)):
            try:
                prompt_text = json.dumps(params["messages"])
            except Exception:
                pass

        if prompt_text and not guardrail_rx.search(prompt_text):
            ptr = (
                deep_pointer(idx, ["parameters", "text"])
                if "text" in params
                else deep_pointer(idx, ["parameters"])
            )
            line, col, _ = lookup_pos(pos_index, ptr, idx)
            findings.append(Finding(
                category="AI / LLM",
                severity="medium",
                rule_id="AI-NO-GUARDRAIL",
                title=(
                    f"AI node '{name}' prompt has no obvious "
                    f"guardrail language"
                ),
                detail=(
                    "None of the configured guardrail keywords "
                    "detected in the prompt."
                ),
                node_name=name, node_type=ntype,
                line=line, col=col, json_pointer=ptr,
                recommendation=(
                    "Add explicit safety / refusal instructions "
                    "and an output-validation step."
                ),
                owasp="OWASP LLM05:2025 (Improper Output Handling)",
                confidence="medium",
            ))

        # 3) Token-bomb risk (threshold from rules)
        if prompt_text and len(prompt_text) > max_prompt_len:
            ptr = (
                deep_pointer(idx, ["parameters", "text"])
                if "text" in params
                else deep_pointer(idx, ["parameters"])
            )
            line, col, _ = lookup_pos(pos_index, ptr, idx)
            findings.append(Finding(
                category="AI / LLM",
                severity="medium",
                rule_id="AI-LARGE-PROMPT",
                title=(
                    f"AI node '{name}' has a very large prompt template "
                    f"({len(prompt_text)} chars)"
                ),
                detail="Large prompts inflate token cost and risk truncation.",
                node_name=name, node_type=ntype,
                line=line, col=col, json_pointer=ptr,
                recommendation=(
                    "Compress / chunk the input; consider RAG "
                    "instead of inlining all data."
                ),
                owasp="OWASP LLM10:2025 (Unbounded Consumption)",
                confidence="medium",
            ))

        # 4) No retryOnFail
        if node.get("retryOnFail") is not True:
            ptr = deep_pointer(idx, [])
            line, col, _ = lookup_pos(pos_index, ptr, idx)
            findings.append(Finding(
                category="AI / LLM",
                severity="low",
                rule_id="AI-NO-RETRY",
                title=f"AI node '{name}' has no retryOnFail",
                detail="LLM APIs frequently rate-limit / 5xx.",
                node_name=name, node_type=ntype,
                line=line, col=col, json_pointer=ptr,
                recommendation=(
                    "Enable retryOnFail with a small delay; "
                    "configure a fallback model."
                ),
                owasp="OWASP LLM10:2025",
                confidence="high",
            ))

    return findings


def audit_error_handling(
    workflow: Dict[str, Any],
    pos_index: Dict[str, Tuple[int, int]],
    risky_patterns: List[Tuple[re.Pattern[str], str]],
) -> List[Finding]:
    findings: List[Finding] = []
    nodes = workflow.get("nodes", []) or []
    settings = workflow.get("settings", {}) or {}

    if not settings.get("errorWorkflow"):
        ptr = "/settings"
        line, col, _ = lookup_pos(pos_index, ptr)
        findings.append(Finding(
            category="Error Handling",
            severity="high",
            rule_id="ERR-NO-ERROR-WORKFLOW",
            title="No Error Workflow configured",
            detail="settings.errorWorkflow is empty.",
            node_name=None, node_type=None,
            line=line, col=col, json_pointer=ptr,
            recommendation=(
                "Create an Error Trigger workflow that logs + notifies "
                "on failures, and attach it here."
            ),
            owasp=None,
            confidence="high",
        ))

    for idx, node in enumerate(nodes):
        ntype = node.get("type") or ""
        name = node.get("name", "")
        for rx, rec in risky_patterns:
            if rx.search(ntype):
                if (
                    node.get("retryOnFail") is not True
                    and node.get("continueOnFail") is not True
                ):
                    ptr = deep_pointer(idx, [])
                    line, col, _ = lookup_pos(pos_index, ptr, idx)
                    findings.append(Finding(
                        category="Error Handling",
                        severity="medium",
                        rule_id="ERR-NO-RETRY-OR-CONTINUE",
                        title=(
                            f"Risky node '{name}' has neither "
                            f"retryOnFail nor continueOnFail"
                        ),
                        detail=f"type = {ntype}",
                        node_name=name, node_type=ntype,
                        line=line, col=col, json_pointer=ptr,
                        recommendation=rec,
                        confidence="high",
                    ))
                break

    return findings


def audit_readability(
    workflow: Dict[str, Any],
    pos_index: Dict[str, Tuple[int, int]],
) -> List[Finding]:
    findings: List[Finding] = []
    nodes = workflow.get("nodes", []) or []
    seen_names: Dict[str, int] = defaultdict(int)

    for idx, node in enumerate(nodes):
        name = node.get("name") or ""
        ntype = node.get("type") or ""
        seen_names[name] += 1

        unclear = (
            not name
            or re.match(r"^Node\d+$", name)
            or len(name.strip()) < 3
            or name.strip().lower() in ("untitled", "default")
        )
        if unclear:
            ptr = deep_pointer(idx, ["name"])
            line, col, _ = lookup_pos(pos_index, ptr, idx)
            findings.append(Finding(
                category="Readability",
                severity="low",
                rule_id="READ-UNCLEAR-NAME",
                title=f"Unclear node name: {name!r}",
                detail="Default or too-short name.",
                node_name=name, node_type=ntype,
                line=line, col=col, json_pointer=ptr,
                recommendation=(
                    "Rename to a descriptive verb-noun phrase "
                    "(e.g. 'Fetch Customers from API')."
                ),
                confidence="high",
            ))

    for name, count in seen_names.items():
        if count > 1 and name:
            for idx, node in enumerate(nodes):
                if node.get("name") == name:
                    ptr = deep_pointer(idx, ["name"])
                    line, col, _ = lookup_pos(pos_index, ptr, idx)
                    findings.append(Finding(
                        category="Readability",
                        severity="low",
                        rule_id="READ-DUPLICATE-NAME",
                        title=(
                            f"Duplicate node name '{name}' "
                            f"({count} occurrences)"
                        ),
                        detail="",
                        node_name=name, node_type=node.get("type"),
                        line=line, col=col, json_pointer=ptr,
                        recommendation=(
                            "Give each node a unique name for "
                            "unambiguous debugging."
                        ),
                        confidence="high",
                    ))
                    break

    return findings


def audit_structural(
    workflow: Dict[str, Any],
    pos_index: Dict[str, Tuple[int, int]],
) -> List[Finding]:
    findings: List[Finding] = []
    nodes = workflow.get("nodes", []) or []
    connections = workflow.get("connections", {}) or {}

    names = {n.get("name") for n in nodes if n.get("name")}
    incoming: set = set()
    for src, conn in connections.items():
        if src not in names:
            continue
        if not isinstance(conn, dict):
            continue
        for _kind, branches in conn.items():
            if not isinstance(branches, list):
                continue
            for branch in branches:
                if not isinstance(branch, list):
                    continue
                for tgt in branch:
                    if isinstance(tgt, dict) and tgt.get("node"):
                        incoming.add(tgt["node"])

    trigger_keywords = (
        "trigger", "webhook", "manualtrigger", "cron", "schedule",
    )
    for idx, node in enumerate(nodes):
        name = node.get("name", "")
        ntype = (node.get("type") or "").lower()
        is_trigger = any(t in ntype for t in trigger_keywords)
        is_sticky = "stickynote" in ntype
        if is_sticky:
            continue

        if not is_trigger and name not in incoming:
            ptr = deep_pointer(idx, [])
            line, col, _ = lookup_pos(pos_index, ptr, idx)
            findings.append(Finding(
                category="Structural",
                severity="medium",
                rule_id="STR-UNREACHABLE",
                title=(
                    f"Node '{name}' is unreachable "
                    f"(no incoming connections)"
                ),
                detail=f"type = {node.get('type')}",
                node_name=name, node_type=node.get("type"),
                line=line, col=col, json_pointer=ptr,
                recommendation="Delete the node or connect it from upstream.",
                confidence="medium",
            ))

        if node.get("disabled") is True:
            ptr = deep_pointer(idx, ["disabled"])
            line, col, _ = lookup_pos(pos_index, ptr, idx)
            findings.append(Finding(
                category="Structural",
                severity="low",
                rule_id="STR-DISABLED-NODE",
                title=f"Node '{name}' is disabled",
                detail="",
                node_name=name, node_type=node.get("type"),
                line=line, col=col, json_pointer=ptr,
                recommendation=(
                    "Remove if dead code; document if intentional."
                ),
                confidence="high",
            ))

    return findings


