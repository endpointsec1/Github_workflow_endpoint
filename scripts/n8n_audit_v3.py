def load_rules(rules_path: Optional[str] = None) -> Optional[Dict[str, Any]]:
    """Load audit rules from YAML. Returns None if file not found or PyYAML
    is not installed."""
    if rules_path is None:
        # Default: look next to this script
        rules_path = os.path.join(
            os.path.dirname(os.path.abspath(__file__)),
            "audit_rules.yaml",
        )
    if not os.path.exists(rules_path):
        return None
    if not HAS_YAML:
        print(
            "WARNING: PyYAML not installed. "
            "audit_rules.yaml found but cannot be read. Using built-in defaults.",
            file=sys.stderr,
        )
        return None
    try:
        with open(rules_path, "r", encoding="utf-8") as f:
            return yaml.safe_load(f)
    except Exception as e:
        print(f"WARNING: Failed to load {rules_path}: {e}", file=sys.stderr)
        return None


