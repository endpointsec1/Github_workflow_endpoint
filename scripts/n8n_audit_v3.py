def build_secret_patterns(
    rules: Optional[Dict[str, Any]],
) -> List[Tuple[str, re.Pattern[str], str, str, str]]:
    """Build secret patterns from rules YAML, falling back to defaults."""
    if not rules or "secret_patterns" not in rules:
        return DEFAULT_SECRET_PATTERNS
    patterns: List[Tuple[str, re.Pattern[str], str, str, str]] = []
    for r in rules["secret_patterns"]:
        try:
            patterns.append((
                r["rule_id"],
                re.compile(r["regex"]),
                r.get("severity", "high"),
                r.get("confidence", "medium"),
                r.get("description", r["rule_id"]),
            ))
        except (re.error, KeyError) as e:
            print(
                f"WARNING: Skipping invalid secret pattern "
                f"{r.get('rule_id', '?')}: {e}",
                file=sys.stderr,
            )
    return patterns if patterns else DEFAULT_SECRET_PATTERNS


def build_risky_patterns(
    rules: Optional[Dict[str, Any]],
) -> List[Tuple[re.Pattern[str], str]]:
    """Build risky node patterns from rules YAML, falling back to defaults."""
    if not rules or "risky_node_patterns" not in rules:
        return DEFAULT_RISKY_TYPE_PATTERNS
    patterns: List[Tuple[re.Pattern[str], str]] = []
    for r in rules["risky_node_patterns"]:
        try:
            patterns.append((
                re.compile(r["pattern"], re.I),
                r.get("recommendation",
                       "Enable Retry on Fail and add an Error Workflow."),
            ))
        except (re.error, KeyError) as e:
            print(
                f"WARNING: Skipping invalid risky pattern: {e}",
                file=sys.stderr,
            )
    return patterns if patterns else DEFAULT_RISKY_TYPE_PATTERNS


def build_secret_rule_ids(
    secret_patterns: List[Tuple[str, re.Pattern[str], str, str, str]],
) -> Set[str]:
    """Build the set of rule IDs that count as hardcoded secrets for the
    verdict engine.  Always includes SEC-SENSITIVE-KEY-LITERAL."""
    ids: Set[str] = {"SEC-SENSITIVE-KEY-LITERAL"}
    for rule_id, _, sev, _, _ in secret_patterns:
        if sev == "high":
            ids.add(rule_id)
    return ids


# Keys whose VALUES are sensitive but legitimately stored in expressions.
SENSITIVE_VALUE_KEYS = re.compile(
    r"^(authorization|apikey|api_key|token|secret|password|client_secret"
    r"|access_token|refresh_token|x-api-key)$",
    re.I,
)


def _is_expression(val: str) -> bool:
    if not isinstance(val, str):
        return False
    return bool(re.search(r"\{\{.*?\}\}|^\=", val))


def _is_credential_ref(val: str) -> bool:
    return isinstance(val, str) and (
        "$credentials" in val or "credentials." in val
    )


def shannon_entropy(s: str) -> float:
    if not s:
        return 0.0
    freqs: Dict[str, int] = defaultdict(int)
    for c in s:
        freqs[c] += 1
    n = len(s)
    return -sum((c / n) * math.log2(c / n) for c in freqs.values())


# ---------------------------------------------------------------------------
# Walkers
# ---------------------------------------------------------------------------


def walk(
    value: Any, path: Optional[List[Any]] = None,
) -> Iterable[Tuple[List[Any], Any]]:
    if path is None:
        path = []
    if isinstance(value, dict):
        yield path, value
        for k, v in value.items():
            yield from walk(v, path + [k])
    elif isinstance(value, list):
        for i, v in enumerate(value):
            yield from walk(v, path + [i])
    else:
        yield path, value


