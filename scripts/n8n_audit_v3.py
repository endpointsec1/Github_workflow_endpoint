#!/usr/bin/env python3
"""
n8n Workflow Auditor v3 (local Python edition, rules-driven)
=============================================================

A static auditor for exported n8n workflow JSON files.

Inspired by https://github.com/christinec-dev/n8n-Audit-Workflow, this v3:
  - Runs entirely locally (no n8n API, no LLM calls required).
  - Audits the workflow JSON itself (not execution history), so it works on
    workflows that have never run.
  - Reports the EXACT LINE NUMBER in the source JSON for every finding,
    so issues are clickable / traceable.
  - Emits a single self-contained styled HTML report.
  - Reads detection rules from audit_rules.yaml (auto-generated by
    update_rules.py) so frameworks stay current without code changes.
  - Falls back gracefully to built-in defaults if no YAML is present.

Categories audited (mapped to OWASP / OWASP-LLM where relevant):
  1. Security & Secrets        (OWASP A04:2025, A07:2025)
  2. Webhook / Trigger Hygiene (OWASP A01:2025, A07:2025)
  3. AI / LLM Risks            (OWASP LLM Top 10 2025)
  4. Error Handling & Resilience
  5. Readability / Naming
  6. Structural / Static Analysis (unreachable nodes, disabled, pinData)
  7. Repo Hygiene flags (pinData shipped, etc.)

Usage:
    python3 n8n_audit_v3.py <workflow.json> [-o report.html] [--rules audit_rules.yaml]

Exit codes:
    0  Verdict PASS
    1  Verdict FAIL
    2  Invalid input / parse error
"""

